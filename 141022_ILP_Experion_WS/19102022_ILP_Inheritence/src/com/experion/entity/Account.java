package com.experion.entity;

import java.util.ArrayList;

public class Account extends Product {
	
	private String accountType;
	private ArrayList<BankingServices> bankServices;
	
	public Account(String productCode, String productName, String productType, String accountType,
			ArrayList<BankingServices> bankServices) {
		super(productCode, productName, productType);
		this.accountType = accountType;
		this.bankServices = bankServices;
		
	}

	public String getAccountType() {
		return accountType;
	}

	public void setAccountType(String accountType) {
		this.accountType = accountType;
	}

	public ArrayList<BankingServices> getBankServices() {
		return bankServices;
	}

	public void setBankServices(ArrayList<BankingServices> bankServices) {
		this.bankServices = bankServices;
	}
	
	
}
